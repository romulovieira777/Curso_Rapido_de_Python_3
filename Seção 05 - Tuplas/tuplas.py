# Criando uma Tupla
nomes = ('Ana', 'Bia', 'Gui', 'Ana')
print(type(nomes))
print(nomes)


# Para saber se um Nome está na Tupla
print('Bia' in nomes)


# Buscando um Dado Específico na Tupla
print(nomes[0])


# Acessando mais de um Dado na Tupla
print(nomes[1:3])
print(nomes[1:-1])
print(nomes[2:])
print(nomes[:-2])


# Verificando o Tamanho da Tupla
print(len(nomes))

