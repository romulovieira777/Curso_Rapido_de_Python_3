# Declarando Duas Variáveis
x = 7
y = 5


# Comparando se X é maior que Y
print(x > y)


# Comparando se X é maior ou igual Y
print(x >= y)


# Comparando se X é menor que Y
print(x < y)


# Comparando se X é menor ou igual Y
print(x <= y)


# Comprando se X é igual Y
print(x == y)


# Comparando se X é diferente de Y
print(x != y)
