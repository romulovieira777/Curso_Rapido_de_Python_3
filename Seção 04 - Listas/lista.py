# Criando uma Lista
numeros = [1, 2, 3]
print(type(numeros))


# Adicionando Dados na Lista Criada
numeros.append(4)
numeros.append(5)
numeros.append(6)
print(len(numeros))


# Alterando um Número da Lista
numeros[5] = 100


# Inserindo Dados na Lista
numeros.insert(6, 7)
print(numeros)


# Selecionando um Dado Específico da Lista
print(numeros[6])


# Pegando o Último Elemento da Lista
print(numeros[-1])
