# Criando um Dicionário
aluno = {
    'nome': 'Pedro Henrique',
    'nota': 9.2,
    'ativo': True
}

print(type(aluno))


# Acessando um Valor Específico do Dicionário
print(aluno['nome'])
print(aluno['nota'])
print(aluno['ativo'])


# Contando Quantos pares Associados dentro do Dicionário
print(len(aluno))
