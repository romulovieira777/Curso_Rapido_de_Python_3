# Declarando Variáveis
x = 10
y = 3


# Somando o Valor das Duas Variáveis
print(x + y)


# Subtraindo o Valor das Duas Variáveis
print(x - y)


# Multiplicando o Valor das Duas Variáveis
print(x * y)


# Dividindo o Valor das Duas Variáveis
print(x / y)


# Pegando o Resto da Divisão
print(x % y)


# Para saber se o Número é Par ou Ímpar
par = 34
impar = 33

print(par % 2 == 0)
print(impar % 2 == 1)
